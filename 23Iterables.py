#Iterables =    An object/collection that can return its elements one at a time
#               allowing it to be iterated over in a loop

#All of the following are iterables
my_list = [1, 2, 3, 4, 5]
my_tuple = (1, 2, 3, 4, 5)
my_set = {"coco", "apple", "banana"}
my_string = "Castle"
my_dictionary = {"a": "b", "c": "d"}

#Which means you can use them in, for example, for loops to return their values
